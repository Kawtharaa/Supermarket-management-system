CREATE SCHEMA superMarket_project;
USE superMarket_project;
CREATE TABLE employee ( phone_no VARCHAR(20), age INT, first_name VARCHAR(45), last_name VARCHAR(45), ssn INT NOT NULL, salary INT,PRIMARY KEY (ssn));
CREATE TABLE bills (price INT, date DATE, emp_id VARCHAR(45),product_code VARCHAR(45), bill_no VARCHAR(45),tax VARCHAR(45),product_no VARCHAR(45),PRIMARY KEY (bill_no));
CREATE TABLE offers (product_code INT,validity VARCHAR(45),type VARCHAR(45),duration VARCHAR(45),date DATE, PRIMARY KEY(product_code));
CREATE TABLE payment (total_price INT,type VARCHAR(45),p_id INT NOT NULL,bill_id INT, PRIMARY KEY (p_id));
CREATE TABLE product (code INT NOT NULL, quantity VARCHAR(45),section VARCHAR(45), name VARCHAR(45), PRIMARY KEY (code));
CREATE TABLE sections (name VARCHAR(45), s_number VARCHAR(45) NOT NULL,emp_id VARCHAR(45), aisle VARCHAR(45),PRIMARY KEY (s_number));
CREATE TABLE shifts (cashier_no INT NOT NULL,time VARCHAR(45),date DATE,emp_id VARCHAR(45),PRIMARY KEY (cashier_no));
